
package by.bsuir.firstlab.myutils;


 public class SmartOutput {

    public static void ShowArray(double[] myArray)
    {
        for(int i=0;i<myArray.length;i++){
            System.out.print(myArray[i]);
            System.out.print(" ");            
        }
            
    }
    public static void ShowArray(int[] myArray)
    {
        for(int i=0;i<myArray.length;i++){
            System.out.print(myArray[i]);
            System.out.print(" ");            
        }
            
    }
    
}
